package se.litsec.bankid.rpapi.state;

/**
 * The {@code InitiateOperationState} is the first state after the user has selected "this" or "another" device.
 * Dependending on the configuration (requirements) and the user device, the next step may be to require additional
 * input, or to start the operation.
 * 
 * @author Martin Lindstr√∂m (martin@litsec.se)
 */
public interface InitiateOperationState extends BankIDState<VoidInput> {

  /**
   * Returns {@code false}. This is not an end state.
   */
  @Override
  default boolean isEndState() {
    return false;
  }
  
  /**
   * Returns {@code false}. No input is required for this state.
   */
  @Override
  default boolean isInputRequired() {
    return false;
  }  
  
  /**
   * Returns {@code null}. No output is generated by this state.
   */
  @Override
  default BankIDStateOutput getOutput() {
    return null;
  }

  /** {@inheritDoc} */
  @Override
  default String getStateName() {
    return "initiate-operation";
  }

}
